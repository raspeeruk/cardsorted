export function getCardBySlug(slug: string): CreditCard | undefined {
  return cards.find((card) => card.slug === slug);
}

export function getCardsByCategory(category: string): CreditCard[] {
  return cards.filter((card) => card.categories.includes(category));
}

export function getCardsByScoreRange(score: number): CreditCard[] {
  return cards.filter(
    (card) => score >= card.creditScoreMin && score <= card.creditScoreMax
  );
}

export function getCardsByCategoryAndScore(
  category: string,
  score: number
): CreditCard[] {
  return cards.filter(
    (card) =>
      card.categories.includes(category) &&
      score >= card.creditScoreMin &&
      score <= card.creditScoreMax
  );
}

export function getTopCards(limit: number = 10): CreditCard[] {
  return [...cards]
    .sort((a, b) => b.commissionEstimate - a.commissionEstimate)
    .slice(0, limit);
}

export function getCardsForComparison(
  slugA: string,
  slugB: string
): [CreditCard, CreditCard] | null {
  const cardA = getCardBySlug(slugA);
  const cardB = getCardBySlug(slugB);
  if (!cardA || !cardB) return null;
  return [cardA, cardB];
}
